#include <stdio.h>
/**
* main - Entry point of the program
*
* Description: Prints all single digit numbers of base 10
* starting from 0, followed by a new line.
*
* Return: Always 0 (Success)
*/
int main(void)
{
int i;
/* Loop through each single digit number */
for (i = 0; i < 10; i++)
{
/* Print the number */
printf("%d", i);
}
/* Print a new line */
printf("\n");
return (0);
}



